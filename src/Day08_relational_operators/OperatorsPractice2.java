package Day08_relational_operators;

public class OperatorsPractice2 {
    public static void main(String[] args) {


        int a = 40;
        System.out.println(--a);
        System.out.println(--a);
        System.out.println(a--);
        System.out.println(--a);
        System.out.println(--a);
        System.out.println(a--);
        System.out.println(a);
    }
}
